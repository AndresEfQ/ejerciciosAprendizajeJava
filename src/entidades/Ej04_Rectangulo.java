package entidades;

public class Ej04_Rectangulo {

    private int base;
    private int altura;

    public Ej04_Rectangulo() {
    }

    public Ej04_Rectangulo(int base, int altura) {
        this.base = base;
        this.altura = altura;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
}
